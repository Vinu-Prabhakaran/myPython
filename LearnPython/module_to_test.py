def caps_text(text):
    '''
    Return the text with first letter capitalized
    :param text:
    :return:
    '''
    return text.capitalize()

def title_text(text):
    '''
    Return the text with first letter of each word capitalized
    :param text:
    :return:
    '''
    return text.title()
